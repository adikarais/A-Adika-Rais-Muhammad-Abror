// 1. String
let String = 'Hello, Musetix!';
console.log('String:', String);

// 2. Boolean
let Boolean = true;
console.log('Boolean:', Boolean);

// 3. Number
let Number = 42;
console.log('Number:', Number);

// 4. Array
let Array = ['Event', 'Komunitas', 'Kategori'];
console.log('Array:', Array);

// 5. BigInt (for very large integers)
let BigInt = 1234567890123456789012345678901234567890n;
console.log('BigInt:', BigInt);

// 6. Null
let Null = null;
console.log('Null:', Null);

// 7. Object
let obj = {
  name: 'Musetix',
  type: 'Event Platform',
  features: ['Events', 'Tickets', 'Community'],
};
console.log('Object:', obj);

// 8. Operator Examples
let tambah = 5 + 3;
let kurang = 5 - 3;
let kali = 5 * 3;
let bagi = 10 / 2;
let modulus = 10 % 3;
console.log('Penambahan (5 + 3):', tambah);
console.log('Pengurangan (5 - 3):', kurang);
console.log('Perkalian (5 * 3):', kali);
console.log('Pembagian (10 / 2):', bagi);
console.log('Modulus (10 % 3):', modulus);
